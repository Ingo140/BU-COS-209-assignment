//ARROW FUNCTION
// An arrow function is a type of function in JavaScript that is defined using the => symbol.
//It provides a shorter and cleaner way to write functions. It does not require the function keyword. 
//If the function has a single expression, it can return the value implicitly without return. 

const add = (a, b) => { return a + b; }; 

console.log(add(5, 3));
